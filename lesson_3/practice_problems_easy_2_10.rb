# The benefits of using Object Oriented Programming in Ruby
# include

# Encapsulation
# * separating interface from implementation, allowing greater
#   flexibility for changing existing code or adding features

# * allowing the grouping of functionality with the data that
#   the functionality operates on, leading to more logical
#   abstractions that more clearly model the problem domain

# * protecting data behind an interface where accessing or changing
#   the state of the program requires more intention than simply
#   passing arguments to functions

# Polymorphism
# * The ability to have multiple types of data respond to a common interface
#   while having the implementation be uniquely appropriate to the
#   types of data involved.

# Inheritance
# * greater facilities for code resuse, resulting in less surface for
#   mistakes and, potentially at least, a greater readability or
#   ease of understanding
# * the ability to model hierarchical relationships between
#   parts of the problem domain

# LS solution
# 1) Creating objects allows programmers to think more abstractly about
#    the code they are writing

# 2) Objects are represented by nouns so are easier to conceptualize

# 3) It allows us to only expose functionality to the parts of code that
#    need it, meaning namespace issues are much harder to come across.

# 4) It allows us to easily give functionality to different parts of
#    an application without duplication

# 5) We can build applications faster as we can reuse pre-written code

# 6) As the software becomes more complex this complexity can be more
#    easily managed